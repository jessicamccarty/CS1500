# print() is used to display output on the console or standard output. It takes one or more arguments, which can be strings, variables, or expressions, and prints them on the screen.

# Display a simple message by passing a string as an argument.
print("Hello, World!")

# Display values or variables by passing them as arguments
name = "John"
age = 25
print(name)
print(age)

# Combine text and variable values in a single print statement using commas. The print() function will automatically insert a space between the arguments.
name2 = "Jason"
print("My name is", name2)

